#include<stdio.h>
void main(){
    int i=200;
    float f=24.57;
    char m='K';
    double d=56.87876788;
    long int l=9876543210;
    printf("Integer: %d\n",i);
    printf("Float: %.2f\n",f);
    printf("Character: %c\n",m);
    printf("Double: %.8lf\n",d);
    printf("Long Integer: %ld\n",l);
}